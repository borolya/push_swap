#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	if ((int)size == -1)
		return (NULL);
	if (!(str = malloc((size + 1) * (sizeof(char)))))
		return (NULL);
	i = 0;
	while (i <= size)
	{
		str[i] = 0;
		i++;
	}
	return (str);
}
