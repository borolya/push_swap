#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*s_char;
	size_t	i;

	s_char = (char*)s;
	i = 0;
	while (i < n)
	{
		*s_char = 0;
		s_char++;
		i++;
	}
}
